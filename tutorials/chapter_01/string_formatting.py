firstname = "christopher"
lastname = "larroum"

print("Hello! "+firstname+" "+lastname+". Hope you are doing good!") # Concatenation 

print("Hello! {} {}. Hope you are doing good!".format(firstname, lastname)) # .format() method

print(f"Hello! {firstname} {lastname}. Hope you are doing good!") # f string

# Hello! christopher larroum. Hope you are doing good!